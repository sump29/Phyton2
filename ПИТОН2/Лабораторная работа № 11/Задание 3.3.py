# Создаем список из 12 вещественных чисел (можно задать любые значения)
numbers = [3.5, 1.2, 6.8, 2.3, 4.7, 0.5, 8.9, 1.1, 9.4, 5.6, 7.2, 2.1]

# Находим индекс наименьшего элемента
min_idx = numbers.index(min(numbers))

# Меняем местами наименьший элемент с первым элементом
numbers[0], numbers[min_idx] = numbers[min_idx], numbers[0]

print(numbers)
