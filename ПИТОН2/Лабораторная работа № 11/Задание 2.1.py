# Генерируем список B из 20 элементов
import random
B = [random.randint(-10, 10) for _ in range(20)]

# Инициализируем переменную для хранения суммы положительных элементов
sum_positive = 0

# Проходимся по всем элементам списка B
for num in B:
    if num > 0:  # Проверяем, является ли элемент положительным
        sum_positive += num  # Если да, то добавляем его к сумме

# Выводим сумму положительных элементов списка B
print(f"Сумма положительных элементов списка B: {sum_positive}")