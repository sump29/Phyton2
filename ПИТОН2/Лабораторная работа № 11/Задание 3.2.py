# Исходный список из 15 вещественных чисел
список = [3.7, 8.2, 5.6, 2.1, 9.4, 4.8, 6.3, 1.5, 7.9, 3.2, 8.7, 6.1, 2.5, 5.9, 10.2]

# Находим индекс наибольшего элемента в списке
индекс_максимального = список.index(max(список))

# Меняем местами последний элемент и элемент с наибольшим значением
список[-1], список[индекс_максимального] = список[индекс_максимального], список[-1]

# Вывод результата
print(список)